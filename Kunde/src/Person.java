public class Person {
    private String name;
    private String nachname;
    private String nummer;

    Person (String name, String nachname){
        this.name = name;
        this.nachname = nachname;
    }

    public String getName() {
        return name;
    }

    public String getNachname() {
        return nachname;
    }

    public String getNummer() {
        return nummer;
    }


}


